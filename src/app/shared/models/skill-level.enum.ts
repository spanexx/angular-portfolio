/**
 * Represents proficiency levels for skills
 */
export enum SkillLevel {
  /** Basic understanding, limited experience */
  Beginner = 'Beginner',
  
  /** Comfortable with core concepts, some real-world experience */
  Intermediate = 'Intermediate',
  
  /** Deep expertise, can solve complex problems */
  Advanced = 'Advanced',
  
  /** Recognized expert, can mentor others */
  Expert = 'Expert'
}